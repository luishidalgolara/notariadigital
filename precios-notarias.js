// Obtener información de una notaría
function obtenerInfoNotaria(notariaId) {
    return NOTARIAS_INFO[notariaId] || null;
}

// Obtener precios de una notaría
function obtenerPreciosNotaria(notariaId) {
    return PRECIOS_POR_NOTARIA[notariaId] || null;
}

// Obtener precio específico de un servicio en una notaría
function obtenerPrecioServicio(notariaId, servicioId) {
    const precios = PRECIOS_POR_NOTARIA[notariaId];
    return precios ? precios[servicioId] : null;
}

// Listar todas las notarías por comuna
function obtenerNotariasPorComuna(comuna) {
    return Object.keys(NOTARIAS_INFO).filter(id => 
        NOTARIAS_INFO[id].comuna === comuna
    );
}

// Generar número de servicio único
function generarNumeroServicio() {
    const timestamp = Date.now().toString().slice(-6);
    const random = Math.floor(Math.random() * 9999).toString().padStart(4, '0');
    return `${timestamp}${random}`;
}

// Validar si existe una notaría
function validarNotaria(notariaId) {
    return NOTARIAS_INFO.hasOwnProperty(notariaId);
}

// Obtener todas las notarías
function obtenerTodasLasNotarias() {
    return Object.keys(NOTARIAS_INFO);
}

// Verificar disponibilidad de servicio en notaría
function servicioDisponible(notariaId, servicioId) {
    const precios = PRECIOS_POR_NOTARIA[notariaId];
    return precios && precios.hasOwnProperty(servicioId);
}

